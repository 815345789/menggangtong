package com.PRD.model;

public class UserPower {
	//权限信息表
	private Integer power_Id;
	private Integer power_Num;
	private String power_Name;
	public Integer getPower_Id() {
		return power_Id;
	}
	public void setPower_Id(Integer power_Id) {
		this.power_Id = power_Id;
	}
	public Integer getPower_Num() {
		return power_Num;
	}
	public void setPower_Num(Integer power_Num) {
		this.power_Num = power_Num;
	}
	public String getPower_Name() {
		return power_Name;
	}
	public void setPower_Name(String power_Name) {
		this.power_Name = power_Name;
	}
	
}
